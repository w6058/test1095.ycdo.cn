namespace Portal\Controller;
use Common\Controller\HomebaseController; 
/**
 * 邮件发送
 */
class SendEmailController extends HomebaseController {

    //邮件发送
	public function index() {
		$content = "姓名：".I("post.InputName")."，邮箱：".I("post.InputEmail").", 电话号码：".I("post.InputPhone").", 最爱的食物：".I("post.InputSubject").", 内容：".I("post.InputMessage")."。";
    	sp_send_email(C('SP_MAIL_LOGINNAME'),'标题',$content);
    	$this->success("邮件发送成功！");
    }

}


